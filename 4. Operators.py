#   ==    !=    >     <     >=     
#   <=    +     -     -=    +=


# // Variable 1 equals 2
var_1 = 2

# // Variable 2 equals 1
var_2 = 1



# // Add to a variable
#
# +
#
var_2 + 1


# // Add from a variable and set value
#
# +=
#
var_1 += 1

var_1 = var_1 + 1



# // Remove from a variable
#
# -
#
var_1 - 1



# // Remove from variable and set value
#
# -=
#
var_1 -= 1

var_1 = var_1 - 1



# // Variable1 is equal to Variable2
#
#  ==
#
var_1 == var_2



# // Variable1 is not equal to Variable2
#
#  !=
#
var_1 != var_2



# // Variable1 is greater than Variable2
#
#  >
#
var_1 > var_2



# // Variable1 is less than Variable2
#
#  <
#
var_1 < var_2



# // Variable1 is greater than or equal to Variable2
#
#  >=
#
var_1 >= var_2



# // Variable1 is less than or equal to Variable2
#
#  <=
#
var_1 <= var_2
